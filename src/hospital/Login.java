    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String user = this.userInput.getText();
        String password = this.passwordInput.getText();
        
        //Validate
        boolean validacion = false;
        
        try {
            File myObj = new File("src/hospital/usuarios.txt");
            Scanner myReader = new Scanner(myObj);
            while (myReader.hasNextLine()) {
              String data = myReader.nextLine();
              
              System.out.println(data);
              String[] datos = data.split("\\|");
              
              if (datos[0].equals(user) & datos[1].equals(password) & datos[2].equals(this.tipo)) {
                  validacion = true;
              }
              
            }
            myReader.close();
        } catch (FileNotFoundException e) {
            System.out.println("An error occurred.");
            e.printStackTrace();
        }
        
        //Opening
        if (this.tipo.equals("Admin") & validacion) {
            javax.swing.JFrame userPanel = new Administrador(user);
            userPanel.setVisible(true);
            this.setVisible(false);
        } else if (this.tipo.equals("Usuario") & validacion) {
            javax.swing.JFrame userPanel = new Panel_de_Usuario(user);
            userPanel.setVisible(true);
            this.setVisible(false);
        } else {
            System.out.println("Usuario no encontrado");
            this.Error.setVisible(true);
        }
    }//GEN-LAST:event_loginActionPerformed


